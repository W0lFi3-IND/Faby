package com.findingbetteryou.faby.caloriecal;

public class CalDetails {
    public String item;
    public String totalcal;


    public CalDetails(String item, String totalcal) {
        this.item = item;
        this.totalcal = totalcal;

    }

    public CalDetails() {

    }

    public String getItem() {
        return item;
    }

    public String getTotalcal() {
        return totalcal;
    }



}


